    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih mobil yang ingin disewa");
            return;
        }
        
        String idMobil = (String) jTable1.getValueAt(selectedRow, 0);
        userSession.setSelectedMobil(idMobil);
        String statusMobil = (String) jTable1.getValueAt(selectedRow, 6);
        
        if(statusMobil.equalsIgnoreCase("disewa")){
            JOptionPane.showMessageDialog(this, "Mobil yang anda pilih sedang disewa orang lain saat ini");
            return;
        }else if(statusMobil.equalsIgnoreCase("maintenance")){
            JOptionPane.showMessageDialog(this, "Mobil yang anda pilih sedang dalam keadaan maintenance");
            return;
        }
        
        new rental().setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

