    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idMobil = jTextField1.getText().trim();
        String merk = jTextField2.getText().trim();
        String model = jTextField3.getText().trim();
        String tahun = jTextField4.getText().trim();
        String noPlat = jTextField5.getText().trim();
        int hargaSewa = Integer.parseInt(jTextField6.getText());
        
        
        
        if (idMobil.isEmpty() || merk.isEmpty() || model.isEmpty() || tahun.isEmpty() || noPlat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi");
            return;
        }
        
        try {
            Connection conn = databaseConnection.connect();
            String cekQuery = "SELECT COUNT(*) FROM mobil WHERE id_mobil = ?";
            PreparedStatement cekStmt = conn.prepareStatement(cekQuery);
            cekStmt.setString(1, idMobil);
            ResultSet rs = cekStmt.executeQuery();
            rs.next();
            if (rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "ID Mobil sudah digunakan!");
                return;
            }
            String insertQuery = "INSERT INTO mobil (id_mobil, merk, model, tahun, no_plat, harga_sewa, status) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(insertQuery);
            stmt.setString(1, idMobil);
            stmt.setString(2, merk);
            stmt.setString(3, model);
            stmt.setString(4, tahun);
            stmt.setString(5, noPlat);
            stmt.setInt(6, hargaSewa);
            stmt.setString(7, gstatus);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jComboBox1.setSelectedIndex(-1);
            
            tampilkanmobil();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

