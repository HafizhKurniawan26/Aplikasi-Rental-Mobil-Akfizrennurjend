    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus");
            return;
        }
        
          int confirm = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus mobil ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            String idMobil = (String) jTable1.getValueAt(selectedRow, 0); // ambil ID di kolom 0

            try (Connection conn = databaseConnection.connect();
                PreparedStatement stmt = conn.prepareStatement("DELETE FROM mobil WHERE id_mobil = ?")) {
                stmt.setString(1, idMobil);
                stmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Mobil berhasil dihapus.");
                tampilkanmobil(); // refresh tabel
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus mobil: " + ex.getMessage());
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

