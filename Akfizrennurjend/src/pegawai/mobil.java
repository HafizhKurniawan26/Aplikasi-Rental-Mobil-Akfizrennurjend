    public void tampilkanmobil() {
        try{
        Connection conn = databaseConnection.connect();
        String sql = "SELECT id_mobil, merk, model, tahun, no_plat, harga_sewa, status FROM mobil";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Mobil");
        model.addColumn("Merk");
        model.addColumn("model");
        model.addColumn("Tahun");
        model.addColumn("No PLat");
        model.addColumn("Harga Sewa");
        model.addColumn("Status");
        
        while (rs.next()){
            model.addRow(new Object[]{
            rs.getString("id_mobil"),
            rs.getString("merk"),
            rs.getString("model"),
            rs.getString("tahun"),
            rs.getString("no_plat"),
            rs.getInt("harga_sewa"),
            rs.getString("Status")
                    
            });
        }
        
        jTable1.setModel(model);
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error: "+ e.getMessage());
        }
    }

