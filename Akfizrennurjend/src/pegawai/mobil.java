     private void updateDataMobil(){
        String idMobil = jTextField1.getText().trim();
        String merk = jTextField2.getText().trim();
        String model = jTextField3.getText().trim();
        String tahun = jTextField4.getText().trim();
        String noPlat = jTextField5.getText().trim();
        int hargaSewa = Integer.parseInt(jTextField6.getText());
        String status = (String) jComboBox1.getSelectedItem();

        if (merk.isEmpty() || model.isEmpty() || tahun.isEmpty() || noPlat.isEmpty() || jComboBox1.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
            return;
        }

        try (Connection conn = databaseConnection.connect()) {
            String sql = "UPDATE mobil SET merk=?, model=?, tahun=?, no_plat=?, harga_sewa=?, status=? WHERE id_mobil=?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, merk);
            stmt.setString(2, model);
            stmt.setString(3, tahun);
            stmt.setString(4, noPlat);
            stmt.setInt(5, hargaSewa);
            stmt.setString(6, status);
            stmt.setString(7, idMobil);
   
           

            int rowsUpdated = stmt.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Data Mobil berhasil diupdate.");
                // Reset form
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                
                jComboBox1.setSelectedIndex(-1);
                jTextField1.setEnabled(true);
             

                // Change button back to save mode
                jButton1.setText("Simpan");
                jButton1.removeActionListener(jButton1.getActionListeners()[0]);
                jButton1.addActionListener(new java.awt.event.ActionListener() {
                    public void actionPerformed(java.awt.event.ActionEvent evt) {
                        jButton1ActionPerformed(evt);
                    }
                });

                tampilkanmobil();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengupdate: " + e.getMessage());
        }
   
    } 

    
