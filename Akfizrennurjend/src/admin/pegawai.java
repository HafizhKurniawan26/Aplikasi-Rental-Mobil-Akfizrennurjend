    public void updateDataPegawai(){
        int id_user = Integer.parseInt(jTextField2.getText().trim());
        String idPegawai = jTextField1.getText().trim();
        String nama = jTextField3.getText().trim();
        String nik = jTextField4.getText().trim();
        String email = jTextField5.getText().trim();
        String alamat = jTextField6.getText().trim();
        String selectedRole = (String) jComboBox1.getSelectedItem();
        String jabatan = (String) jComboBox2.getSelectedItem();
        String gaji = jTextField7.getText().trim();

        if (nama.isEmpty() || jComboBox1.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
            return;
        }

        try (Connection conn = databaseConnection.connect()) {
            String sqlUser = "UPDATE users SET nama=?, nik=?, email=?, alamat=?, role=? WHERE id_user=?";
            PreparedStatement stmt = conn.prepareStatement(sqlUser);
            stmt.setString(1, nama);
            stmt.setString(2, nik);
            stmt.setString(3, email);
            stmt.setString(4, alamat);
            stmt.setString(5, selectedRole);
            stmt.setInt(6, id_user);
            
            
            String sqlPegawai = "UPDATE pegawai SET jabatan=?, gaji=? WHERE id_pegawai=?";
            PreparedStatement stmt2 = conn.prepareStatement(sqlPegawai);
            stmt2.setString(1, jabatan);
            stmt2.setString(2, gaji);
            stmt2.setString(3, idPegawai);
   
           

            int rowsUpdated = stmt.executeUpdate();
            int rowsUpdated2 = stmt2.executeUpdate();

            if (rowsUpdated > 0 && rowsUpdated2 > 0) {
                JOptionPane.showMessageDialog(this, "Data Pegawai berhasil diupdate.");
                // Reset form
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
                
                jComboBox1.setSelectedIndex(-1);
                jComboBox2.setSelectedIndex(-1);
                jTextField1.setEnabled(true);
             

                // Change button back to save mode
                jButton1.setText("Simpan");
                jButton1.removeActionListener(jButton1.getActionListeners()[0]);
                jButton1.addActionListener(new java.awt.event.ActionListener() {
                    public void actionPerformed(java.awt.event.ActionEvent evt) {
                        jButton1ActionPerformed(evt);
                    }
                });

                tampilkanPegawai();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengupdate: " + e.getMessage());
        }
    }
    
