    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idPegawai = jTextField1.getText().trim();
        int idUser = Integer.parseInt(jTextField2.getText());
        String jabatan = gjabatan;
        int gaji = Integer.parseInt(jTextField7.getText());
        
        try {
            Connection conn = databaseConnection.connect();
            String cekQuery = "SELECT COUNT(*) FROM pegawai WHERE id_pegawai = ?";
            PreparedStatement cekStmt = conn.prepareStatement(cekQuery);
            cekStmt.setString(1, idPegawai);
            ResultSet rs = cekStmt.executeQuery();
            rs.next();
            if (rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "ID Pegawai sudah terdaftar!");
                return;
            }
            String insertQuery = "INSERT INTO pegawai (id_pegawai, id_user, jabatan, gaji) VALUES (?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(insertQuery);
            stmt.setString(1, idPegawai);
            stmt.setInt(2, idUser);
            stmt.setString(3, jabatan);
            stmt.setInt(4, gaji);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
        
            jComboBox1.setSelectedIndex(-1);
            jComboBox2.setSelectedIndex(-1);
            
            tampilkanPegawai();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

